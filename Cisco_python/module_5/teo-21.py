#El método swapcase()
#El método swapcase() crea una nueva cadena intercambiando todas las letras por mayúsculas o minúsculas dentro de la cadena original: 
#los caracteres en mayúscula se convierten en minúsculas y viceversa.

#Todos los demás caracteres permanecen intactos.

#Observa el primer ejemplo en el editor. ¿Puedes adivinar la salida?
# Demostración del método swapcase()
print("Yo sé que no sé nada.".swapcase())

print()

#yO SÉ QUE NO SÉ NADA.

#El método title()
#El método title() realiza una función algo similar cambia la primera letra de cada palabra a mayúsculas, 
#convirtiendo todas las demás a minúsculas.

#Mira el segundo ejemplo en el editor. ¿Puedes adivinar su salida? Este es el resultado:
# Demostración del método title()
print("Yo sé que no sé nada. Parte 1.".title())

print()

#Yo Sé Que No Sé Nada. Parte 1.

#El método upper()
#Por último, pero no menos importante, el método upper() hace una copia de la cadena de origen, 
#reemplaza todas las letras minúsculas con sus equivalentes en mayúsculas, y devuelve la cadena como resultado.

#Mira el tercer ejemplo en el editor. Produce:
# Demostración del método upper()
print("Yo sé que no sé nada. Parte 2.".upper())

#YO SÉ QUE NO SÉ NADA. PARTE 2.

